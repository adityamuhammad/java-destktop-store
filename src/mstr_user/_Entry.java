    private void b_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_simpanActionPerformed
        try {
            m.Cari(txt_id.getText());
            m.user_id = txt_id.getText();
            m.nama = txt_nm.getText();
            m.jenis_kelamin = cb_jk.getSelectedItem().toString();
            m.tempat_lahir = txt_t4.getText();
            m.tanggal_lahir = t.format(jXDatePicker1.getDate());
            m.alamat = txt_alt.getText();
            m.password = txt_passw.getText();
            if(m._Akses.equals("-")) {
                m.updateData();
                txt_id.setEditable(false);
                JOptionPane.showMessageDialog(null,"Data berhasil di ubah");
            } else {
                m.simpanData();
                txt_id.setEditable(false);
                JOptionPane.showMessageDialog(null,"Data berhasil disimpan");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan" +e);
        }
    }//GEN-LAST:event_b_simpanActionPerformed

